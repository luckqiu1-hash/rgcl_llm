import argparse
import csv
import os
import random
import re
from statistics import mean, pstdev


RECORD_PATTERN = re.compile(
    r"^id=(?P<id>[^\t]+)\tlabel=(?P<label>[01])\tpred=(?P<pred>[01])\tprob=(?P<prob>[-+0-9.eE]+)\t"
)


def parse_args():
    parser = argparse.ArgumentParser(
        description="Evaluate accuracy under random validation/test splits from a RAC result txt."
    )
    parser.add_argument(
        "--res_txt",
        type=str,
        default="./src/toixic_mm_res.txt",
        help="Prediction result txt generated by test_rac.py.",
    )
    parser.add_argument(
        "--output_csv",
        type=str,
        default="./src/random_split_acc.csv",
        help="Where to save per-seed split accuracy.",
    )
    parser.add_argument(
        "--seeds",
        type=str,
        default="0-999",
        help='Seeds to evaluate, e.g. "0-99" or "0,1,2,42".',
    )
    parser.add_argument(
        "--test_ratio",
        type=float,
        default=0.5,
        help="Ratio assigned to the random test split. The rest is validation.",
    )
    parser.add_argument(
        "--stratify",
        type=lambda x: str(x).lower() == "true",
        default=True,
        help="Keep label distribution approximately unchanged in each split.",
    )
    return parser.parse_args()


def parse_seeds(seed_text):
    seed_text = seed_text.strip()
    if "-" in seed_text and "," not in seed_text:
        start, end = seed_text.split("-", 1)
        return list(range(int(start), int(end) + 1))
    return [int(item.strip()) for item in seed_text.split(",") if item.strip()]


def load_records(path):
    records = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            match = RECORD_PATTERN.match(line.strip())
            if not match:
                continue
            label = int(match.group("label"))
            pred = int(match.group("pred"))
            prob = float(match.group("prob"))
            records.append(
                {
                    "id": match.group("id"),
                    "label": label,
                    "pred": pred,
                    "prob": prob,
                    "correct": int(label == pred),
                }
            )

    if not records:
        raise ValueError(f"No prediction records found in {path}")

    seen_ids = set()
    duplicate_ids = []
    for record in records:
        if record["id"] in seen_ids:
            duplicate_ids.append(record["id"])
        seen_ids.add(record["id"])
    if duplicate_ids:
        raise ValueError(f"Duplicate ids found in result file, first duplicate: {duplicate_ids[0]}")

    return records


def split_records(records, seed, test_ratio, stratify):
    rng = random.Random(seed)
    if not stratify:
        shuffled = list(records)
        rng.shuffle(shuffled)
        test_size = round(len(shuffled) * test_ratio)
        return shuffled[test_size:], shuffled[:test_size]

    val_records = []
    test_records = []
    for label in sorted({record["label"] for record in records}):
        group = [record for record in records if record["label"] == label]
        rng.shuffle(group)
        test_size = round(len(group) * test_ratio)
        test_records.extend(group[:test_size])
        val_records.extend(group[test_size:])

    rng.shuffle(val_records)
    rng.shuffle(test_records)
    return val_records, test_records


def accuracy(records):
    if not records:
        return 0.0
    return sum(record["correct"] for record in records) / len(records)


def auc(records):
    pos_scores = [record["prob"] for record in records if record["label"] == 1]
    neg_scores = [record["prob"] for record in records if record["label"] == 0]
    if not pos_scores or not neg_scores:
        return 0.0

    wins = 0.0
    for pos_score in pos_scores:
        for neg_score in neg_scores:
            if pos_score > neg_score:
                wins += 1.0
            elif pos_score == neg_score:
                wins += 0.5
    return wins / (len(pos_scores) * len(neg_scores))


def label_count(records, label):
    return sum(1 for record in records if record["label"] == label)


def evaluate(records, seeds, test_ratio, stratify):
    rows = []
    for seed in seeds:
        val_records, test_records = split_records(records, seed, test_ratio, stratify)
        rows.append(
            {
                "seed": seed,
                "val_size": len(val_records),
                "test_size": len(test_records),
                "val_pos": label_count(val_records, 1),
                "test_pos": label_count(test_records, 1),
                "val_acc": accuracy(val_records),
                "val_auc": auc(val_records),
                "test_acc": accuracy(test_records),
                "test_auc": auc(test_records),
                "all_acc": accuracy(records),
                "all_auc": auc(records),
            }
        )
    return rows


def save_rows(rows, output_csv):
    os.makedirs(os.path.dirname(os.path.abspath(output_csv)), exist_ok=True)
    with open(output_csv, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(
            f,
            fieldnames=[
                "seed",
                "val_size",
                "test_size",
                "val_pos",
                "test_pos",
                "val_acc",
                "val_auc",
                "test_acc",
                "test_auc",
                "all_acc",
                "all_auc",
            ],
        )
        writer.writeheader()
        for row in rows:
            writer.writerow(
                {
                    **row,
                    "val_acc": f'{row["val_acc"]:.6f}',
                    "val_auc": f'{row["val_auc"]:.6f}',
                    "test_acc": f'{row["test_acc"]:.6f}',
                    "test_auc": f'{row["test_auc"]:.6f}',
                    "all_acc": f'{row["all_acc"]:.6f}',
                    "all_auc": f'{row["all_auc"]:.6f}',
                }
            )


def print_summary(records, rows, output_csv):
    val_accs = [row["val_acc"] for row in rows]
    test_accs = [row["test_acc"] for row in rows]
    val_aucs = [row["val_auc"] for row in rows]
    test_aucs = [row["test_auc"] for row in rows]
    best_test = max(rows, key=lambda row: row["test_acc"])
    worst_test = min(rows, key=lambda row: row["test_acc"])
    best_test_auc = max(rows, key=lambda row: row["test_auc"])
    worst_test_auc = min(rows, key=lambda row: row["test_auc"])

    print(f"Loaded records: {len(records)}")
    print(f"Overall acc in result file: {accuracy(records):.6f}")
    print(f"Overall auc in result file: {auc(records):.6f}")
    print(f"Seeds: {rows[0]['seed']} ... {rows[-1]['seed']} ({len(rows)} total)")
    print(f"Val acc mean/std: {mean(val_accs):.6f} / {pstdev(val_accs):.6f}")
    print(f"Test acc mean/std: {mean(test_accs):.6f} / {pstdev(test_accs):.6f}")
    print(f"Val auc mean/std: {mean(val_aucs):.6f} / {pstdev(val_aucs):.6f}")
    print(f"Test auc mean/std: {mean(test_aucs):.6f} / {pstdev(test_aucs):.6f}")
    print(
        "Best test acc: seed={seed}, test_acc={test_acc:.6f}, test_auc={test_auc:.6f}, val_acc={val_acc:.6f}, val_auc={val_auc:.6f}".format(
            **best_test
        )
    )
    print(
        "Worst test acc: seed={seed}, test_acc={test_acc:.6f}, test_auc={test_auc:.6f}, val_acc={val_acc:.6f}, val_auc={val_auc:.6f}".format(
            **worst_test
        )
    )
    print(
        "Best test auc: seed={seed}, test_auc={test_auc:.6f}, test_acc={test_acc:.6f}, val_auc={val_auc:.6f}".format(
            **best_test_auc
        )
    )
    print(
        "Worst test auc: seed={seed}, test_auc={test_auc:.6f}, test_acc={test_acc:.6f}, val_auc={val_auc:.6f}".format(
            **worst_test_auc
        )
    )
    print(f"Saved per-seed results to: {os.path.abspath(output_csv)}")


def main():
    args = parse_args()
    if not 0 < args.test_ratio < 1:
        raise ValueError("--test_ratio must be between 0 and 1")

    records = load_records(args.res_txt)
    seeds = parse_seeds(args.seeds)
    rows = evaluate(records, seeds, args.test_ratio, args.stratify)
    save_rows(rows, args.output_csv)
    print_summary(records, rows, args.output_csv)


if __name__ == "__main__":
    main()
