def set_text(p, text):
    p.clear()
    p.add_run(text)


def replace_first(doc, prefix, text):
    for p in doc.paragraphs:
        if p.text.strip().startswith(prefix):
            set_text(p, text)
            return True
    raise ValueError(f"missing paragraph prefix: {prefix}")


